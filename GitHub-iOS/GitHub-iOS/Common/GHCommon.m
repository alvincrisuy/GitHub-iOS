#import "GHCommon.h"

static GHCommon* _sharedInstance = nil;

@implementation GHCommon

+ (GHCommon *)sharedInstance {
    
    @synchronized(self) {
        if (_sharedInstance == nil) {
            _sharedInstance = [[self alloc] init];
        }
    }
    
    return _sharedInstance;
}

- (instancetype)init {
    
    if (self = [super init]) {
        [self setASCII];
    }
    
    return self;
}

